    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
                                       

    // ===== 1. CHECK NGHIỆP VỤ =====
    if (jTable1.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Cart is empty!");
        return;
    }

    if (cus_id.equals("0")) {
        JOptionPane.showMessageDialog(this, "Please select customer!");
        return;
    }

    Connection con = null;

    try {
        con = ConnectionDB.getConnection();
        con.setAutoCommit(false);   // TRANSACTION

        // ===== 2. SAVE CART =====
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        int rc = dt.getRowCount();

        String cartSQL =
            "INSERT INTO cart (INID, p_name, Bar_code, Qty, Unit_Price, Total_Price) "
          + "VALUES (?, ?, ?, ?, ?, ?)";

        PreparedStatement cartPst = con.prepareStatement(cartSQL);

        for (int i = 0; i < rc; i++) {
            cartPst.setInt(1, Integer.parseInt(dt.getValueAt(i, 0).toString()));
            cartPst.setString(2, dt.getValueAt(i, 1).toString());
            cartPst.setString(3, dt.getValueAt(i, 2).toString());
            cartPst.setInt(4, Integer.parseInt(dt.getValueAt(i, 3).toString()));
            cartPst.setDouble(5, Double.parseDouble(dt.getValueAt(i, 4).toString()));
            cartPst.setDouble(6, Double.parseDouble(dt.getValueAt(i, 5).toString()));
            cartPst.executeUpdate();
        }

        // ===== 3. CALC PAYMENT =====
        int inv_id = Integer.parseInt(inid.getText());
        int cid = Integer.parseInt(cus_id.trim());
        int totqty = Integer.parseInt(tot_qty.getText());
        double tot_bill = Double.parseDouble(bill_total.getText());

        String paidText = paid_amt.getText();
        if (paidText.isEmpty()) paidText = "0";
        double paid = Double.parseDouble(paidText);

        double blc = tot_bill - paid;

        String Status;
        if (paid == 0) {
            Status = "Unpaid";
        } else if (paid < tot_bill) {
            Status = "Partial";
        } else {
            Status = "Paid";
        }

        // ===== 4. SAVE SALES =====
        String salesSQL =
            "INSERT INTO Sales (INID, cid, c_name, Total_Qty, Total_Bill, Status, Balance) "
          + "VALUES (?, ?, ?, ?, ?, ?, ?)";

        PreparedStatement salesPst = con.prepareStatement(salesSQL);

        salesPst.setInt(1, inv_id);
        salesPst.setInt(2, cid);
        salesPst.setString(3, com_cus.getSelectedItem().toString());
        salesPst.setInt(4, totqty);
        salesPst.setDouble(5, tot_bill);
        salesPst.setString(6, Status);
        salesPst.setDouble(7, blc);

        salesPst.executeUpdate();

        // ===== 5. UPDATE LAST INVOICE =====
        PreparedStatement extraPst =
            con.prepareStatement("UPDATE extra SET val=? WHERE exid=1");
        extraPst.setInt(1, inv_id);
        extraPst.executeUpdate();

        con.commit();   // OK

        JOptionPane.showMessageDialog(this, "Sale completed successfully");
        //=======Print============
        printInvoice();
        // ===== 6. CLEAR FORM =====
        ((DefaultTableModel) jTable1.getModel()).setRowCount(0);
        paid_amt.setText("");
        balance.setText("");

    } catch (Exception e) {
        try {
            if (con != null) con.rollback();
        } catch (Exception ex) {}
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    //
    // ===== 6. KIỂM TRA THANH TOÁN =====
double paid = Double.parseDouble(paid_amt.getText().isEmpty() ? "0" : paid_amt.getText());
double tot_bill = Double.parseDouble(bill_total.getText());

if (paid >= tot_bill) {
    // thanh toán đầy đủ → cho in
    printInvoice(); 
} else {
    JOptionPane.showMessageDialog(this, "Bill chưa thanh toán đủ. Không thể in hóa đơn!");
}


    }//GEN-LAST:event_jButton4ActionPerformed

