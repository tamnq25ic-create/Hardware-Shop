   private void loadInvoiceId() {
        try {
            Connection con = ConnectionDB.getConnection();
            Statement s = con.createStatement();
            ResultSet rs = s.executeQuery(
                "SELECT val FROM extra WHERE exid = 1"
            );

            if (rs.next()) {
                int last = rs.getInt("val");
                inid.setText(String.valueOf(last + 1));
            } else {
                inid.setText("1");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
    
